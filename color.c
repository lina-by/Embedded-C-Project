#include <xc.h>
#include "color.h"
#include "i2c.h"

void color_click_init(void)
{   
    //setup colour sensor via i2c interface
    I2C_2_Master_Init();      //Initialise i2c Master

     //set device PON
	 color_writetoaddr(0x00, 0x01);
    __delay_ms(3); //need to wait 3ms for everthing to start up
    
    //turn on device ADC and Interrupt
	color_writetoaddr(0x00, 0b00010011);
    
    //set integration time
	color_writetoaddr(0x01, 0xF6);
    
    // Setting up interrupt register values 
    color_writetoaddr(0x04, 0b00000000); // Low threshold low byte
    color_writetoaddr(0x05, 0b00000000); // Low threshold high byte
    color_writetoaddr(0x06, 0xFF); // High threshold low byte
    color_writetoaddr(0x07, 0xFF); // High threshold high byte
    
    // Interrupt Persistance value
    color_writetoaddr(0x0C, 0b00000100); // 5 Clear consecutive channel output
}

void clear_interrupt(void)
{
    I2C_2_Master_Start();         //Start condition
    I2C_2_Master_Write(0x52 | 0x00);     //7 bit device address + Write mode
    I2C_2_Master_Write(0b11100110);     // Reset colour clicker interrupt
    I2C_2_Master_Stop();          //Stop condition
}

void set_high_threshold(unsigned int threshold)
{
    color_writetoaddr(0x06, (unsigned char)(threshold)); // High threshold low byte
    color_writetoaddr(0x07, (threshold >> 8)); // High threshold high byte
}

void color_writetoaddr(char address, char value){
    I2C_2_Master_Start();         //Start condition
    I2C_2_Master_Write(0x52 | 0x00);     //7 bit device address + Write mode
    I2C_2_Master_Write(0x80 | address);    //command + register address
    I2C_2_Master_Write(value);    
    I2C_2_Master_Stop();          //Stop condition
}

unsigned int color_read_Red(void)
{
	unsigned int tmp;
	I2C_2_Master_Start();         //Start condition
	I2C_2_Master_Write(0x52 | 0x00);     //7 bit address + Write mode
	I2C_2_Master_Write(0xA0 | 0x16);    //command (auto-increment protocol transaction) + start at RED low register
	I2C_2_Master_RepStart();			// start a repeated transmission
	I2C_2_Master_Write(0x52 | 0x01);     //7 bit address + Read (1) mode
	tmp=I2C_2_Master_Read(1);			//read the Red LSB
	tmp=tmp | (I2C_2_Master_Read(0)<<8); //read the Red MSB (don't acknowledge as this is the last read)
	I2C_2_Master_Stop();          //Stop condition
	return tmp;
}
    
